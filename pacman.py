#Definición de funciones para el juego

#Función Para el tablero (recuadro)
def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

#Función para los puntos que recolecta pacman (los quita)
def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

#Función para desaparecer los puntos un vez que pacman los come
def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

#Fucnión para poner al mundo (paredes y corredores)
def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

#Función que define el movimiento de los fantasmas y de pacman así como los puntos (score)
def move():
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    #Cambio de posición de Pacman al sumarle el valor de aim
    if valid(pacman + aim):
        #Se llama a la función move para cambiar de posición a Pacman
        pacman.move(aim)

    #Se llama a la función offset para ajustar los puntos recolectados
    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    #Velocidad de Pacman
    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    #Movimiento de los fantasmas
    for point, course in ghosts:
        #Validando si se comio al punto para mover al mismo
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y
        #Velocidad de los fantasmas y su color
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    #Varifica si pacman no ha ganado por puntos acumulados
    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

#Cambio de la posición de pacman
def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y
        
